print("Nicolas", "Henrique")
# Resultado que será impresso na tela: Nicolas Henrique.
# Padrão do comando o uso da vírgular servir como espaçamento.

print("Nicolas", "Henrique", sep="")
# Resultado que será impressoa na tela: NicolasHenrique.

print("Nicolas", "Henrique", sep="-")
# Resultado que será impresso na tela: Nicolas-Henrique.

print("Nicolas", "Henrique", end=".")
# Resultado que será impresso na tela: Nicolas-Henrique. (Com ponto final)

