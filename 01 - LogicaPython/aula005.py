"""
Introdução do operador lógico igual (==), aqui ele verifica se dois dados são iguais ou não
e a partir desse dado, futuramente, mudar o fluxo do programa para diferentes caminhos.
"""

print(10 == 10)
# Resultado que será impresso na tela: True

print(10 == 9)
# Resultado que será impresso na tela: False

print(type(10 == 10))
# Resultado que será impresso na tela: <class 'bool'>